//aritmeticos: + - / % * ()

const a = 11;
const b = 5;

console.log(a + b);
console.log(a % 5);

//relacionales: > < >= <= <> === != !==

console.log(8 > 9);
console.log(a == "11"); // compara solo valor
console.log(a === "11"); // compara tipo de dato y valor

//de incremento o decremento: += -=

let i = 1;
console.log(i);
i += 1;
console.log(i);

//operador unario ++ --

i++;
console.log(i);
i--;
console.log(i);
--i;
console.log(i);

//logicos:  ! || &&

console.log(!true);
console.log(5 + 5 < 5 && 3 + 3 > 3);
console.log(5 + 5 < 5 || 3 + 3 > 3);
